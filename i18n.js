const RTL_LANGS = ['ar'];

// Language display names
const LANG_NAMES = {
  en: 'EN',
  zh: '\u4e2d\u6587',
  ja: '\u65e5\u672c\u8a9e',
  ko: '\ud55c\uad6d\uc5b4',
  ar: '\u0627\u0644\u0639\u0631\u0628\u064a\u0629',
  fr: 'FR',
  de: 'DE',
  hi: '\u0939\u093f\u0902\u0926\u0940',
};

// Detect user language
function detectLang() {
  const saved = localStorage.getItem('airclap-lang');
  if (saved && TRANSLATIONS[saved]) return saved;

  const nav = navigator.language || navigator.userLanguage || 'en';
  const code = nav.split('-')[0].toLowerCase();
  return TRANSLATIONS[code] ? code : 'en';
}

// Apply translations
function setLang(lang) {
  const t = TRANSLATIONS[lang];
  if (!t) return;

  localStorage.setItem('airclap-lang', lang);

  // Set dir for RTL
  const isRTL = RTL_LANGS.includes(lang);
  document.documentElement.dir = isRTL ? 'rtl' : 'ltr';
  document.documentElement.lang = lang;

  // Translate all data-i18n elements
  document.querySelectorAll('[data-i18n]').forEach(el => {
    const key = el.getAttribute('data-i18n');
    if (t[key] !== undefined) {
      if (el.tagName === 'INPUT' || el.tagName === 'TEXTAREA') {
        el.placeholder = t[key];
      } else {
        el.innerHTML = t[key];
      }
    }
  });

  // Update platform-aware CTA
  updatePlatformCTA(lang);

  // Update active state in switcher
  document.querySelectorAll('.lang-switcher__option').forEach(opt => {
    opt.classList.toggle('active', opt.dataset.lang === lang);
  });
}

// Platform-aware hero CTA (i18n version)
function updatePlatformCTA(lang) {
  const t = TRANSLATIONS[lang];
  const ua = navigator.userAgent || navigator.vendor || '';
  const p = navigator.platform || '';
  let textKey = 'hero_btn';
  let href = '#download';

  if (/iPhone|iPad|iPod/.test(ua)) {
    textKey = 'cta_iphone';
    href = 'https://apps.apple.com/us/app/airclap/id6467128147';
  } else if (/Mac/.test(p) && !/iPhone/.test(ua)) {
    textKey = 'cta_mac';
    href = 'https://apps.apple.com/us/app/airclap/id6467128147';
  } else if (/Win/.test(p)) {
    textKey = 'cta_windows';
    href = 'https://www.microsoft.com/store/productId/9N19C4QDKR6D';
  } else if (/Android/.test(ua)) {
    textKey = 'cta_android';
    href = 'https://play.google.com/store/apps/details?id=tech.gentleflow.airclap.pro';
  }

  const btn = document.getElementById('heroBtn');
  const btnText = document.getElementById('heroBtnText');
  if (btn) btn.href = href;
  if (btnText) btnText.textContent = t[textKey];
}

// Build language switcher
function buildLangSwitcher() {
  const container = document.querySelector('.lang-switcher');
  if (!container) return;

  const current = detectLang();

  // Current language button
  const btn = document.createElement('button');
  btn.className = 'lang-switcher__btn';
  btn.setAttribute('aria-label', 'Change language');
  btn.innerHTML = `<svg width="16" height="16" viewBox="0 0 24 24" fill="none" stroke="currentColor" stroke-width="1.5"><circle cx="12" cy="12" r="10"/><path d="M2 12h20"/><path d="M12 2a15.3 15.3 0 014 10 15.3 15.3 0 01-4 10 15.3 15.3 0 01-4-10 15.3 15.3 0 014-10z"/></svg><span>${LANG_NAMES[current]}</span>`;

  // Dropdown
  const dropdown = document.createElement('div');
  dropdown.className = 'lang-switcher__dropdown';

  Object.keys(LANG_NAMES).forEach(code => {
    const opt = document.createElement('button');
    opt.className = 'lang-switcher__option' + (code === current ? ' active' : '');
    opt.dataset.lang = code;
    opt.textContent = LANG_NAMES[code];
    opt.addEventListener('click', () => {
      setLang(code);
      btn.querySelector('span').textContent = LANG_NAMES[code];
      dropdown.classList.remove('open');
    });
    dropdown.appendChild(opt);
  });

  btn.addEventListener('click', (e) => {
    e.stopPropagation();
    dropdown.classList.toggle('open');
  });

  document.addEventListener('click', () => dropdown.classList.remove('open'));

  container.appendChild(btn);
  container.appendChild(dropdown);
}

// Init
document.addEventListener('DOMContentLoaded', () => {
  buildLangSwitcher();
  setLang(detectLang());
});
